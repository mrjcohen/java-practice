package day18_while_dowhile_loops;

public class Print0to100 {
	public static void main(String[] args) {
		
		//count from 1 to 101
		
		int counter = 0;
		
		while (counter <= 100) {
			
			counter++;
			System.out.println(counter);
			
			
		}
		
		System.out.println("Counter: "+counter);     //101
		
		
	}
}
