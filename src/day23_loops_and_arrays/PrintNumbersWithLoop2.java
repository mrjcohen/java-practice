package day23_loops_and_arrays;

public class PrintNumbersWithLoop2 {
	public static void main(String[] args) {
		
		
		for (int j = 1 ; j <= 10 ; j++) {
			
			for (int i = 1 ; i <= j ; i++) {
				
				System.out.print(i);
			}
			System.out.println();
		}
		
//		1
//		12
//		123
//		1234
//		12345
//		123456
//		1234567
//		12345678
//		123456789
//		12345678910
		
		
		
		
		
		
		for (int z = 10 ; z >= 1 ; z--) {
			
			for (int x = 1 ; x <= z ; x++) {
				
				System.out.print(x);
			}
			System.out.println();
		}
		
//		12345678910
//		123456789
//		12345678
//		1234567
//		123456
//		12345
//		1234
//		123
//		12
//		1

		
		
		
		
		
		
		
		
		
	}

}

