package day06_operators2;

public class IncrementDecrement {
	public static void main(String[] args) {
		
		int employees = 50;									//				=50
		System.out.println("Employees: " + employees);
		
		employees++;										//Add 1			=51
		System.out.println("Employees: " + employees);
		
		employees += 1;										//Add 1			=52
		System.out.println("Employees: " + employees);
		
		employees = employees + 1;							//Add 1			=53
		System.out.println("Employees: " + employees);
		
		employees--;										//Subtract 1	=52
		System.out.println("Employees: " + employees);
		
		--employees;										//Subtract 1	=51
		System.out.println("Employees: " + employees);
		
		
		
		
		
		
	}

}
